package PruebasJava.src.Datos;

public class Varibales_NoPrimitivo {
    public static void main(String[] args){
        // Otro tipo de dato + permite más métodos
        Integer numero = null;
        System.out.println(numero);
        // Cadenas de texto
        String palabra = "Hola que tal";
        System.out.println(palabra);
        // Constantes (No cambian)
        final int num = 10;
        System.out.println("Soy una constante" + num);
    }
}
